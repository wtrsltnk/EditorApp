// SelectionArgs.cpp: implementation of the SelectionArgs class.
//
//////////////////////////////////////////////////////////////////////

#include "SelectionArgs.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

SelectionArgs::SelectionArgs(RenderArgs* args, SelectionHandler* handler, int x, int y, int w, int h)
{
	if (w <= 0)
		w = 1;
	if (h <= 0)
		h = 1;

	this->renderArgs = args;
	this->selectionHandler = handler;

	this->x = x;
	this->y = y;
	this->width = w;
	this->height = h;
}

SelectionArgs::~SelectionArgs()
{
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
RenderArgs* SelectionArgs::RenderArguments()
{
	return this->renderArgs;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
unsigned int *SelectionArgs::SelectionBuffer()
{
	return this->selectionBuffer;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
int SelectionArgs::BufferSize()
{
	return MAX_BUFFER;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
int SelectionArgs::X()
{
	return this->x;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
int SelectionArgs::Y()
{
	return this->y;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
int SelectionArgs::Width()
{
	return this->width;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
int SelectionArgs::Height()
{
	return this->height;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
int SelectionArgs::GatherHits()
{
	if (this->selectionHandler != 0)
	{
		return this->selectionHandler->GatherHits(this->selectionBuffer);
	}
	return 0;
}